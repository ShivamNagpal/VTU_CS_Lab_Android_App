package com.nagpal.shivam.vtucslab.Model;

public class Laboratory {
    private String name;
    private String url;

    public String getName() {
        return name;
    }

    public Laboratory setName(String name) {
        this.name = name;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public Laboratory setUrl(String url) {
        this.url = url;
        return this;
    }

}
