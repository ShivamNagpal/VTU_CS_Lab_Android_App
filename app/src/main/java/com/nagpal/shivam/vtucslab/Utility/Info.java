package com.nagpal.shivam.vtucslab.Utility;

public class Info {
    private String mTitle;
    private String mUrl;

    public Info(String title, String url) {
        mTitle = title;
        mUrl = url;
    }

    public String getTitle() {
        return mTitle;
    }

    public String getUrl() {
        return mUrl;
    }
}
